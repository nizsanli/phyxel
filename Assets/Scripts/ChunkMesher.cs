    static Vector3 VertexInterp(float iso, Vector3 p1, Vector3 p2, float valp1, float valp2)
    {
        float mu;
        Vector3 p;

        if (Mathf.Abs(iso - valp1) < 10e-5f)
            return p1;
        if (Mathf.Abs(iso - valp2) < 10e-5f)
            return p2;
        if (Mathf.Abs(valp1 - valp2) < 10e-5f)
            return p1;

        mu = (iso - valp1) / (valp2 - valp1);
        p.x = p1.x + mu * (p2.x - p1.x);
        p.y = p1.y + mu * (p2.y - p1.y);
        p.z = p1.z + mu * (p2.z - p1.z);

        return p;
    }
